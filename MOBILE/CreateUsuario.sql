CREATE TABLE USUARIO (
	ID_USUARIO uniqueidentifier NOT NULL DEFAULT (newid()),
	NOME varchar(100) NOT NULL,
	CPF varchar(11) NOT NULL,
	RG_UF int NOT NULL,
	RG_NR varchar(8) NOT NULL,
	DT_NASCIMENTO datetime NOT NULL,
	DT_INCLUSAO datetime NOT NULL,
	DT_ALTERACAO datetime,
	DT_APROVACAO datetime,
	DT_ATIVACAO datetime,
	DT_ORDENACAO datetime NOT NULL,
	APROVADO bit NOT NULL,
	ATIVO bit NOT NULL,
	BLOQUEADO bit NOT NULL DEFAULT ((0)),
	UF int NOT NULL DEFAULT ((0)),
	CEP varchar(8) NOT NULL DEFAULT (''),
	LOGRADOURO varchar(254) NOT NULL DEFAULT (''),
	COMPLEMENTO varchar(254),
	BAIRRO varchar(254) NOT NULL DEFAULT (''),
	CIDADE varchar(254) NOT NULL DEFAULT (''),
	FIXO varchar(14),
	CELULAR varchar(14),
	EMAIL varchar(254) NOT NULL DEFAULT (''),
	CONSTRAINT "PK_jobup.USUARIO" PRIMARY KEY (ID_USUARIO)
) 
